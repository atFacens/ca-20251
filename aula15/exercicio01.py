#  Escreva uma função chamada calcular_media, que recebe três números como parâmetros
#  e retorna a média desses números. 
#  Em seguida, escreva um código que chame essa função com diferentes conjuntos de 
#  valores e exiba os resultados.

def calcular_media(n1, n2, n3):
    resultado = (n1 + n2 + n3) / 3
    return resultado

print(calcular_media(10, 20, 30))

